# Double-precision floating-point numbers
num1 = 3.141592653589793
num2 = 2.718281828459045

# Basic arithmetic operations
sum_result = num1 + num2
difference_result = num1 - num2
product_result = num1 * num2
division_result = num1 / num2

print("Sum:", sum_result)
print("Difference:", difference_result)
print("Product:", product_result)
print("Division:", division_result)

# You can also use the math module for more advanced mathematical operations
import math

sqrt_result = math.sqrt(num1)
power_result = math.pow(num1, 2)
sin_result = math.sin(num1)
cos_result = math.cos(num1)

print("Square Root:", sqrt_result)
print("Power (num1^2):", power_result)
print("Sine:", sin_result)
print("Cosine:", cos_result)
